def number_to_string(n):
    """
    Transform an integer into a string.

    Ways to achieve this in Python:
    1. str(n)           - built-in, most Pythonic
    2. f"{n}"           - f-string
    3. "{}".format(n)   - format method
    4. "%d" % n         - % formatting (legacy)
    """
    return str(n)
